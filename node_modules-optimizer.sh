#!/bin/bash

################################################################################
# NODE_MODULES OPTIMIZER
# Optimize disk space untuk multi-app Node.js deployment
#
# Strategy:
# - repos/               → npm ci (full: dev + prod deps)
# - staging/             → symlink to repos/node_modules
# - production/          → npm ci --production (prod deps only)
#
# Result: ~600MB per app instead of 1.5GB = 60% disk savings
#
# Usage:
#   bash node_modules-optimizer.sh [app_name] [action]
#   
#   action:
#     init       - Setup symlinks (one-time)
#     report     - Show current setup & symlink status
#     revert     - Remove symlinks (restore to independent installs)
#
# Examples:
#   bash node_modules-optimizer.sh aplikasi1 init
#   bash node_modules-optimizer.sh aplikasi1 report
#   bash node_modules-optimizer.sh all init        (all apps)
################################################################################

set -e

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m'

# Functions
print_header() {
    echo -e "\n${BLUE}=== $1 ===${NC}\n"
}

print_success() {
    echo -e "${GREEN}✓ $1${NC}"
}

print_warning() {
    echo -e "${YELLOW}⚠ $1${NC}"
}

print_error() {
    echo -e "${RED}✗ $1${NC}"
}

print_info() {
    echo -e "${CYAN}ℹ $1${NC}"
}

# Configuration
APPS_DIR="$HOME/apps"
DEV_USER="${SUDO_USER:-$(whoami)}"

# ============================================================================
# HELPER FUNCTIONS
# ============================================================================

validate_app() {
    local app_name=$1
    
    if [ ! -d "$APPS_DIR/repos/$app_name" ]; then
        print_error "App not found: $APPS_DIR/repos/$app_name"
        return 1
    fi
    
    return 0
}

get_disk_size() {
    local path=$1
    if [ -d "$path" ]; then
        du -sh "$path" 2>/dev/null | awk '{print $1}'
    else
        echo "0B"
    fi
}

setup_staging_symlink() {
    local app=$1
    local repos_nm="$APPS_DIR/repos/$app/node_modules"
    local staging_nm="$APPS_DIR/staging/$app/node_modules"
    
    print_info "Setting up symlink: staging → repos for $app"
    
    # Check if repos node_modules exists
    if [ ! -d "$repos_nm" ]; then
        print_warning "repos node_modules not found: $repos_nm"
        print_info "Creating repos node_modules first..."
        
        cd "$APPS_DIR/repos/$app"
        
        if [ -f "package.json" ]; then
            npm ci > /dev/null 2>&1
            print_success "npm ci completed for repos/$app"
        else
            print_error "No package.json found in repos/$app"
            return 1
        fi
    fi
    
    # Remove staging node_modules if exists
    if [ -d "$staging_nm" ]; then
        if [ -L "$staging_nm" ]; then
            # Already a symlink
            print_info "Symlink already exists for $app"
            return 0
        else
            # Regular directory, remove it
            print_warning "Removing existing staging node_modules for $app (can take a while)..."
            rm -rf "$staging_nm" 2>/dev/null || sudo rm -rf "$staging_nm"
            print_success "Removed old staging node_modules"
        fi
    fi
    
    # Create symlink
    mkdir -p "$APPS_DIR/staging/$app"
    ln -s "$repos_nm" "$staging_nm"
    
    print_success "Symlink created: $staging_nm → $repos_nm"
}

setup_prod_install() {
    local app=$1
    local prod_dir="$APPS_DIR/production/$app"
    
    print_info "Setting up production install for $app (prod deps only)"
    
    if [ ! -f "$prod_dir/package.json" ]; then
        print_warning "No package.json in production/$app"
        print_info "Checking if package.json exists in repos/$app..."
        
        if [ -f "$APPS_DIR/repos/$app/package.json" ]; then
            # Copy package.json and package-lock.json from repos
            cp "$APPS_DIR/repos/$app/package.json" "$prod_dir/"
            
            if [ -f "$APPS_DIR/repos/$app/package-lock.json" ]; then
                cp "$APPS_DIR/repos/$app/package-lock.json" "$prod_dir/"
            fi
            
            print_success "Copied package files from repos"
        else
            print_error "No package.json found anywhere"
            return 1
        fi
    fi
    
    # Remove old production node_modules if exists
    if [ -d "$prod_dir/node_modules" ]; then
        if [ -L "$prod_dir/node_modules" ]; then
            rm "$prod_dir/node_modules"
        else
            print_warning "Removing existing production node_modules for $app..."
            rm -rf "$prod_dir/node_modules" 2>/dev/null || sudo rm -rf "$prod_dir/node_modules"
        fi
    fi
    
    # Install production dependencies only
    cd "$prod_dir"
    npm ci --production > /dev/null 2>&1
    
    print_success "npm ci --production completed for production/$app"
}

show_status() {
    local app=$1
    
    print_info "Node_modules status for: $app"
    echo ""
    
    # Repos
    local repos_nm="$APPS_DIR/repos/$app/node_modules"
    local repos_size=$(get_disk_size "$repos_nm")
    echo "Repos:"
    echo "  Path: $repos_nm"
    echo "  Size: $repos_size"
    echo "  Type: $([ -d "$repos_nm" ] && echo "Directory" || echo "Not found")"
    echo ""
    
    # Staging
    local staging_nm="$APPS_DIR/staging/$app/node_modules"
    local staging_type="Not found"
    local staging_size="0B"
    
    if [ -L "$staging_nm" ]; then
        staging_type="Symlink"
        staging_size="0B (symlink)"
        local target=$(readlink "$staging_nm")
    elif [ -d "$staging_nm" ]; then
        staging_type="Directory"
        staging_size=$(get_disk_size "$staging_nm")
    fi
    
    echo "Staging:"
    echo "  Path: $staging_nm"
    echo "  Size: $staging_size"
    echo "  Type: $staging_type"
    if [ -L "$staging_nm" ]; then
        echo "  Target: $target"
    fi
    echo ""
    
    # Production
    local prod_nm="$APPS_DIR/production/$app/node_modules"
    local prod_size=$(get_disk_size "$prod_nm")
    echo "Production:"
    echo "  Path: $prod_nm"
    echo "  Size: $prod_size"
    echo "  Type: $([ -d "$prod_nm" ] && echo "Directory" || echo "Not found")"
    echo ""
    
    # Summary
    local total=$(du -sh "$APPS_DIR/repos/$app" "$APPS_DIR/staging/$app" "$APPS_DIR/production/$app" 2>/dev/null | awk '{s+=$1} END {print s}')
    echo -e "${CYAN}Total disk for $app: $total${NC}"
}

revert_symlinks() {
    local app=$1
    
    print_warning "Reverting symlinks for: $app"
    
    local staging_nm="$APPS_DIR/staging/$app/node_modules"
    local repos_nm="$APPS_DIR/repos/$app/node_modules"
    
    if [ -L "$staging_nm" ]; then
        print_info "Removing symlink and installing independent copy..."
        rm "$staging_nm"
        
        cd "$APPS_DIR/staging/$app"
        if [ -f "package.json" ]; then
            npm ci > /dev/null 2>&1
            print_success "Independent node_modules installed for staging/$app"
        fi
    else
        print_info "No symlink found for $app (nothing to revert)"
    fi
}

process_all_apps() {
    local action=$1
    
    print_header "Processing ALL apps"
    
    local apps_list=$(ls -d "$APPS_DIR/repos"/*/ 2>/dev/null | xargs -n1 basename)
    
    if [ -z "$apps_list" ]; then
        print_error "No apps found in $APPS_DIR/repos"
        return 1
    fi
    
    local total_apps=$(echo "$apps_list" | wc -l)
    local processed=0
    
    for app in $apps_list; do
        ((processed++))
        echo ""
        print_info "[$processed/$total_apps] Processing: $app"
        
        case $action in
            init)
                setup_staging_symlink "$app" || true
                setup_prod_install "$app" || true
                ;;
            report)
                show_status "$app"
                ;;
            revert)
                revert_symlinks "$app"
                ;;
        esac
    done
    
    echo ""
    print_success "Processed $processed apps"
}

# ============================================================================
# MAIN LOGIC
# ============================================================================

print_header "NODE_MODULES OPTIMIZER"

# Parse arguments
if [ $# -lt 1 ]; then
    echo "Usage: $0 [app_name|all] [action]"
    echo ""
    echo "Actions:"
    echo "  init       - Setup symlinks & production install (default)"
    echo "  report     - Show current node_modules status"
    echo "  revert     - Remove symlinks, restore independent installs"
    echo ""
    echo "Examples:"
    echo "  $0 aplikasi1 init"
    echo "  $0 all report"
    echo "  $0 aplikasi2 revert"
    exit 0
fi

APP_NAME=$1
ACTION=${2:-init}

# Validate action
case $ACTION in
    init|report|revert) ;;
    *)
        print_error "Unknown action: $ACTION"
        exit 1
        ;;
esac

# Process
if [ "$APP_NAME" == "all" ]; then
    process_all_apps "$ACTION"
else
    if ! validate_app "$APP_NAME"; then
        exit 1
    fi
    
    print_header "Processing: $APP_NAME"
    
    case $ACTION in
        init)
            setup_staging_symlink "$APP_NAME"
            setup_prod_install "$APP_NAME"
            echo ""
            show_status "$APP_NAME"
            ;;
        report)
            show_status "$APP_NAME"
            ;;
        revert)
            revert_symlinks "$APP_NAME"
            ;;
    esac
fi

print_success "Done!"
