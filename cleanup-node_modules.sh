#!/bin/bash

################################################################################
# CLEANUP NODE_MODULES
# Remove unused or stale node_modules installations
#
# Scenarios:
# 1. Remove production-only from staging (if using independent copy)
# 2. Remove repos node_modules (keep only staging/production)
# 3. Force cleanup before re-optimization
#
# Usage:
#   bash cleanup-node_modules.sh [app_name] [target] [--force]
#
#   target:
#     repos       - Remove repos/app/node_modules
#     staging     - Remove staging/app/node_modules
#     production  - Remove production/app/node_modules
#     all         - Remove all three
#
# Examples:
#   bash cleanup-node_modules.sh aplikasi1 staging
#   bash cleanup-node_modules.sh aplikasi1 all --force
#   bash cleanup-node_modules.sh all repos
################################################################################

set -e

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m'

# Functions
print_header() {
    echo -e "\n${BLUE}=== $1 ===${NC}\n"
}

print_success() {
    echo -e "${GREEN}✓ $1${NC}"
}

print_warning() {
    echo -e "${YELLOW}⚠ $1${NC}"
}

print_error() {
    echo -e "${RED}✗ $1${NC}"
}

print_info() {
    echo -e "${CYAN}ℹ $1${NC}"
}

# Configuration
APPS_DIR="$HOME/apps"
FORCE_DELETE=false

# ============================================================================
# HELPER FUNCTIONS
# ============================================================================

validate_app() {
    local app_name=$1
    
    if [ ! -d "$APPS_DIR/repos/$app_name" ] && \
       [ ! -d "$APPS_DIR/staging/$app_name" ] && \
       [ ! -d "$APPS_DIR/production/$app_name" ]; then
        print_error "App not found: $app_name"
        return 1
    fi
    
    return 0
}

get_disk_size() {
    local path=$1
    if [ -d "$path" ]; then
        du -sh "$path" 2>/dev/null | awk '{print $1}'
    else
        echo "0B"
    fi
}

cleanup_target() {
    local app=$1
    local target=$2
    local target_path=""
    
    case $target in
        repos)
            target_path="$APPS_DIR/repos/$app/node_modules"
            ;;
        staging)
            target_path="$APPS_DIR/staging/$app/node_modules"
            ;;
        production)
            target_path="$APPS_DIR/production/$app/node_modules"
            ;;
        *)
            print_error "Unknown target: $target"
            return 1
            ;;
    esac
    
    if [ ! -e "$target_path" ]; then
        print_info "Target doesn't exist: $target_path"
        return 0
    fi
    
    # Get size before
    local size_before=$(get_disk_size "$target_path")
    
    # Check if it's a symlink
    if [ -L "$target_path" ]; then
        local target_link=$(readlink "$target_path")
        print_warning "Target is symlink: $target_link"
        
        if [ "$FORCE_DELETE" = false ]; then
            read -p "Remove symlink? (y/n): " confirm
            if [ "$confirm" != "y" ]; then
                print_info "Cancelled"
                return 0
            fi
        fi
        
        rm "$target_path"
        print_success "Symlink removed: $target_path"
    else
        # Regular directory
        print_info "Removing directory: $target_path"
        print_warning "This will take time for large directories..."
        
        if [ "$FORCE_DELETE" = false ]; then
            read -p "Continue? (y/n): " confirm
            if [ "$confirm" != "y" ]; then
                print_info "Cancelled"
                return 0
            fi
        fi
        
        rm -rf "$target_path"
        print_success "Directory removed: $target_path"
    fi
    
    # Confirm deletion
    if [ ! -e "$target_path" ]; then
        echo -e "  ${GREEN}Size freed: $size_before${NC}"
    else
        print_error "Failed to delete: $target_path"
        return 1
    fi
}

cleanup_app() {
    local app=$1
    local target=$2
    
    print_header "Cleaning up: $app / $target"
    
    if ! validate_app "$app"; then
        return 1
    fi
    
    case $target in
        all)
            print_info "Removing all node_modules for $app"
            cleanup_target "$app" "repos" || true
            cleanup_target "$app" "staging" || true
            cleanup_target "$app" "production" || true
            ;;
        repos|staging|production)
            cleanup_target "$app" "$target"
            ;;
        *)
            print_error "Unknown target: $target"
            return 1
            ;;
    esac
}

cleanup_all_apps() {
    local target=$1
    
    print_header "Cleaning all apps ($target)"
    
    local apps_list=$(ls -d "$APPS_DIR/repos"/*/ 2>/dev/null | xargs -n1 basename)
    
    if [ -z "$apps_list" ]; then
        print_error "No apps found"
        return 1
    fi
    
    local count=0
    for app in $apps_list; do
        print_info "Processing: $app"
        cleanup_app "$app" "$target" || true
        ((count++))
        echo ""
    done
    
    print_success "Processed $count apps"
}

# ============================================================================
# MAIN LOGIC
# ============================================================================

print_header "CLEANUP NODE_MODULES"

# Parse arguments
if [ $# -lt 1 ]; then
    echo "Usage: $0 [app_name|all] [target] [--force]"
    echo ""
    echo "Targets:"
    echo "  repos       - Remove repos/app/node_modules"
    echo "  staging     - Remove staging/app/node_modules"
    echo "  production  - Remove production/app/node_modules"
    echo "  all         - Remove all three"
    echo ""
    echo "Flags:"
    echo "  --force     - Don't ask for confirmation"
    echo ""
    echo "Examples:"
    echo "  $0 aplikasi1 staging"
    echo "  $0 aplikasi1 all --force"
    echo "  $0 all repos"
    exit 0
fi

APP_NAME=$1
TARGET=${2:-all}

# Parse flags
for arg in "$@"; do
    case $arg in
        --force)
            FORCE_DELETE=true
            ;;
    esac
done

# Process cleanup
if [ "$APP_NAME" == "all" ]; then
    if [ "$FORCE_DELETE" = false ]; then
        echo -e "${YELLOW}WARNING: This will remove $TARGET node_modules from ALL apps${NC}"
        read -p "Continue? (y/n): " confirm
        if [ "$confirm" != "y" ]; then
            print_info "Cancelled"
            exit 0
        fi
    fi
    
    cleanup_all_apps "$TARGET"
else
    cleanup_app "$APP_NAME" "$TARGET"
fi

print_success "Cleanup complete!"
